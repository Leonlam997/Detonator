package com.leon.detonator.Bean;

public class UploadDetonatorBean {
    private String DSC;
    private int BlastDelayTime;
    private int BlastRow;
    private int BlastHole;
    private int BlastInside;

    public String getDSC() {
        return DSC;
    }

    public void setDSC(String DSC) {
        this.DSC = DSC;
    }

    public int getBlastDelayTime() {
        return BlastDelayTime;
    }

    public void setBlastDelayTime(int blastDelayTime) {
        BlastDelayTime = blastDelayTime;
    }

    public int getBlastRow() {
        return BlastRow;
    }

    public void setBlastRow(int blastRow) {
        BlastRow = blastRow;
    }

    public int getBlastHole() {
        return BlastHole;
    }

    public void setBlastHole(int blastHole) {
        BlastHole = blastHole;
    }

    public int getBlastInside() {
        return BlastInside;
    }

    public void setBlastInside(int blastInside) {
        BlastInside = blastInside;
    }
}
